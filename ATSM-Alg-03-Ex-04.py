po = int(input("Informe o número de lados do seu polígono: "))


if po == 3 :
    print("Triangulo")
elif po == 4 :
    print("Quadrilatero")
elif po == 5 :
    print("Pentagono")
elif po == 6 :
    print("Hexagono")
elif po == 7 :
    print("Heptagono")
elif po == 8 :
    print("Octogno")
elif po == 9 :
    print("Eneagono")
elif po == 10:
    print("Decagono")
else :
    print("O programa não aceita esses valores")